const PHYSICIAN_TASK = 'Medical Knowledge: Applying Foundational Science Concepts'

function q({
  id,
  subject,
  system,
  concept,
  topic,
  contentArea,
  subdomain,
  angle,
  stem,
  correct,
  distractors,
  explanation,
  pearl,
  trap,
  physicianTask = PHYSICIAN_TASK,
  sourceRefs = [],
}) {
  const fullExplanation = [
    explanation,
    `Clinical pearl: ${pearl}`,
    `Key distinction: ${trap}`,
  ].filter(Boolean).join(' ')

  return {
    id,
    subject,
    system,
    difficulty: 'Balanced',
    testedConcept: concept,
    weakSpotCategory: topic,
    usmleContentArea: contentArea,
    usmleSubdomain: subdomain || topic,
    physicianTask,
    topic,
    questionAngle: angle,
    memoryAnchor: pearl,
    commonTrap: trap,
    sourceRefs,
    stem,
    options: [
      { letter: 'A', text: correct },
      { letter: 'B', text: distractors[0] },
      { letter: 'C', text: distractors[1] },
      { letter: 'D', text: distractors[2] },
    ],
    correct: 'A',
    explanation: fullExplanation,
    optionExplanations: {
      A: fullExplanation,
      B: `${distractors[0]} is incorrect because it does not match the key mechanism: ${concept}. ${trap}`,
      C: `${distractors[1]} is incorrect because it does not explain the clinical finding tested here: ${concept}.`,
      D: `${distractors[2]} is incorrect because the vignette is best explained by ${concept}, not this distractor.`,
    },
    pearl,
  }
}

